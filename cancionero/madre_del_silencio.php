

<p align="center"><strong>359. MADRE DEL SILENCIO </strong></p>
<p>Como una tarde tranquila,<br />
  como un suave atardecer,<br />
  era tu vida sencilla<br />
  en el pobre Nazareth;<br />
  y en medio de aquel silencio,<br />
  Dios te hablaba al coraz&oacute;n.</p>
<p><strong>Virgen Mar&iacute;a, Madre del Se&ntilde;or:<br />
  danos tu silencio y paz<br />
  para escuchar su voz.<br />
  Danos tu silencio y paz<br />
  para escuchar su voz.</strong></p>
<p>Ens&eacute;&ntilde;anos, Madre buena,<br />
  c&oacute;mo se debe escuchar<br />
  al Se&ntilde;or cuando nos habla<br />
  en una noche estrellada,<br />
  en la tierra que, dormida,<br />
  hoy descansa en su bondad.</p>
<p>Y sobre todo, Mar&iacute;a,<br />
  cuando nos habla en los hombres:<br />
  en el hermano que sufre,<br />
  en la sonrisa del ni&ntilde;o,<br />
  en la mano del amigo,<br />
  y en la paz de una oraci&oacute;n.</p>
