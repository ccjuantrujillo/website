<p align="center"><strong>332. MI VIRGEN BELLA</strong></p>
  <p>T&uacute; eres la esperanza que a nosotros, <br />
    en forma de mujer, mand&oacute; el Se&ntilde;or, <br />
    le pides con paciencia que su enojo, <br />
    no caiga sobre el mundo pecador. <br />
    Tan puro es tu amor y tan hermoso, <br />
    que, humildes, nos postramos a tus <br />
    pies. No me atrevo a mirarte a los <br />
    ojos, que indigno que me siento <br />
  ante tu ser.</p>
  <p><strong>Como quisiera, &iexcl;ayy!, mi <br />
  Virgen Bella, que mi cari&ntilde;o se <br />
  convirtiera en una ofrenda y as&iacute; <br />
  entreg&aacute;rtelo. Reina del cielo, la <br />
  m&aacute;s amable, como un lucero <br />
  maravilloso, que siempre est&aacute;, <br />
  Cuid&aacute;ndonos.</strong></p>
  <p>Hoy ante tu altar, y en tu presencia, <br />
    te quiero regalar mi coraz&oacute;n. Va <br />
    lleno de oraciones y promesas, <br />
    para que se lo entregues al Se&ntilde;or.</p>
  <p>T&uacute; eres el orgullo de mi tierra, <br />
    Per&uacute; t&uacute; lo escogiste para ser la <br />
    casa donde al pobre le pudieras <br />
    tu amor y bendiciones conceder.</p>
