  <p>Que	destruyan	en	la	tierra,<br />
    la injusticia y falsedad, que <br />
    construyan un ma&ntilde;ana, lleno <br />
    de fraternidad.</p>
  <p>Que en lugar de la miseria, den <br />
    al hombre dignidad, y liberen al <br />
    esclavo, con trabajo y dignidad.</p>
