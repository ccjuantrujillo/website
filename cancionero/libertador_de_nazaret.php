
  <p align="center"><strong>4. LIBERTADOR DE NAZARET </strong></p>
  <p><strong>Libertador de Nazaret, ven <br />
  junto a m&iacute;, ven junto a m&iacute;! <br />
  Libertador de Nazaret, &iquest;Qu&eacute; <br />
  puedo hacer sin ti?</strong></p>
  <p>Yo	s&eacute;	que	eres	camino,	que<br />
    eres la vida y la verdad; yo s&eacute; <br />
    que el que te sigue sabe a d&oacute;nde <br />
    va. Quiero vivir tu vida, seguir tus <br />
    huellas, tener tu luz; quiero beber <br />
    tu c&aacute;liz, quiero llevar tu cruz.</p>
  <p>Quiero encender mi fuego, <br />
    alumbrar mi vida y seguirte a Ti, <br />
    quiero escucharte siempre, quiero <br />
    luchar por Ti. Busco un mensaje <br />
    nuevo, te necesito, Libertador; no <br />
    puedo estar sin rumbo, no puedo <br />
    estar sin Dios.<br />
  </p>

