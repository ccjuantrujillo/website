
	<p align="center"><strong>13. UN PUEBLO QUE CAMINA </strong></p>
	<p><strong>Un pueblo, camina por el <br />
  mundo, gritando: &iexcl;Ven, Se&ntilde;or! <br />
    Un pueblo que busca en esta <br />
    vida, la gran liberaci&oacute;n.</strong></p>
    <p>Los pobres siempre esperan el <br />
      amanecer, de un d&iacute;a m&aacute;s justo y <br />
      sin opresi&oacute;n; los pobres hemos <br />
      puesto la esperanza en ti, Libertador</p>
    <p>Salvaste nuestra vida de la <br />
      esclavitud, esclavos de la ley, <br />
      sirviendo en el temor; nosotros <br />
      hemos puesto la esperanza en ti, <br />
      Dios del Amor.</p>
    <p>El mundo  por  la  guerra  sangra<br />
      sin raz&oacute;n,   familias   destrozadas<br />
      buscan un hogar; el mundo tiene <br />
      puesta su esperanza en ti, Dios<br />
      de la Paz.<br />
    </p>
