
  <p align="center"><strong>97. SABER QUE VENDRAS </strong></p>
  <p>En este mundo que Cristo nos da, <br />
    hacemos la ofrenda del pan; el <br />
    pan de nuestro trabajo sin fin, y <br />
    el vino de nuestra oblaci&oacute;n. Traigo <br />
    ante Ti, nuestra justa inquietud, <br />
  amar la justicia y la paz.</p>
  <p><strong>Saber  que  vendr&aacute;s,  saber  que<br />
  estar&aacute;s,  partiendo  a  los  pobres<br />
    tu pan (2v).</strong></p>
  <p>Estas ofrendas, de vino y de pan,<br />
    que	hoy	presentamos	con	fe,<br />
    ser&aacute;n tu cuerpo y sangre Se&ntilde;or, <br />
    por tu sacrificio de amor. Es la<br />
    promesa   que hiciste   al partir:<br />
    &ldquo;Con   vosotros, siempre estar&eacute;&rdquo;</p>
  <p>Recibe	nuestras	fatigas	Se&ntilde;or,<br />
    las	penas	y	el	triste	llorar,	las<br />
    alegr&iacute;as  el  gozo  y  la paz,  y  el<br />
    fruto de nuestra   oraci&oacute;n. Con<br />
    nuestra madre, queremos traer,<br />
    la  ofrenda de nuestro vivir.</p>
  <p>La sed de todos los hombres sin <br />
    luz, la pena y el triste llorar; el <br />
    odio, de los que mueren sin fe, <br />
    cansados de tanto luchar. En <br />
    la patena, de nuestra oblaci&oacute;n, <br />
    acepta la vida, Se&ntilde;or</p>
