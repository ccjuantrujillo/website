
  <p align="center"><strong>27. CANTA TROVADOR </strong></p>
  <p>Canta trovador la Buena Nueva <br />
    del Se&ntilde;or, canta a todo el mundo, <br />
    el mensaje que &Eacute;l nos dio. Lleva <br />
    el evangelio en este c&aacute;ntico de <br />
    amor, am&eacute;monos hermanos como <br />
    Cristo nos am&oacute;.</p>
  <p><strong>Canta, canta trovador, entona <br />
  esta canci&oacute;n, y lleva tu cantar, <br />
  al Se&ntilde;or, y as&iacute; todos cantar&aacute;n <br />
    el canto de hermandad, que <br />
    viene del Se&ntilde;or.</strong></p>
  <p>Canta trovador que somos hijos <br />
    del Se&ntilde;or, canta que si todos nos <br />
    unimos es mejor, que la Iglesia <br />
    somos uno y todos en amor, la <br />
    Iglesia peregrina que en Pedro <br />
    Dios fund&oacute;.</p>
  <p>Canta trovador que el hombre <br />
    es libre porque Dios, puso en <br />
    nuestras mentes el milagro de su <br />
    amor, libertad de vida, libertad de <br />
    vocaci&oacute;n, la libertad de hacer la <br />
  paz, la guerra o el amor.</p>


