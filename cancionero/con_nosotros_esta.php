
  <p align="center"><strong>183. CON NOSOTROS ESTA </strong></p>
  <p><strong>Con nosotros est&aacute;, y no le <br />
  conocemos, con nosotros est&aacute;, <br />
  su nombre es el Se&ntilde;or (2v).</strong></p>
  <p>Su nombre es el Se&ntilde;or, y pasa <br />
    hambre, y clama por la boca del <br />
    hambriento; y muchos que lo ven <br />
    pasan de largo, acaso por llegar <br />
    temprano al templo.</p>
  <p>Su nombre es el Se&ntilde;or, y sed <br />
    soporta, y est&aacute; en quien de <br />
    justicia va sediento; y muchos<br />
    que  lo ven pasan de  largo,  a<br />
    veces ocupados   en sus rezos.</p>
  <p>Su	nombre	es	el	Se&ntilde;or	y	est&aacute;<br />
    desnudo, la ausencia   del amor<br />
    hiela  sus huesos, y  muchos que<br />
    lo ven  pasan de largo,  seguros<br />
    y al  calor de su dinero.</p>
  <p>Su nombre es el se&ntilde;or y enfermo <br />
    vive, y su agon&iacute;a es la del enfermo <br />
    y muchos que lo saben no hacen <br />
    caso, tal vez no frecuentaban <br />
    mucho al templo.</p>
  <p>Su nombre es el Se&ntilde;or y est&aacute; en <br />
    la c&aacute;rcel, y est&aacute; en la soledad de <br />
    cada preso; y nadie lo visita y <br />
    hasta dicen: Tal vez &eacute;se, no era <br />
    de los nuestros.</p>
  <p>Su  nombre  es  el  Se&ntilde;or, el que<br />
    sed tiene,  quien  pide  por la boca<br />
    del	hambriento,	est&aacute;	preso,	est&aacute;<br />
    enfermo, est&aacute;  desnudo, pero  &Eacute;l<br />
    nos va  a juzgar,  por todo eso.</p>
