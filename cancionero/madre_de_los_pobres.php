
  <p align="center"><strong>232. MADRE DE LOS POBRES</strong></p>
  <p>Madre    de    los    pobres,    de<br />
    los peregrinos te pedimos por <br />
    Am&eacute;rica Latina. Tierra que visitas <br />
    con los pies descalzos, apretando <br />
  fuerte un ni&ntilde;o en tus brazos.</p>
  <p><strong>&iexcl;Am&eacute;rica despierta! Sobre tus <br />
    montes despunta, la luz de<br />
    una ma&ntilde;ana nueva. D&iacute;a de la <br />
    salvaci&oacute;n, que ya se acerca, <br />
    sobre los pueblos que est&aacute;n <br />
    en tinieblas, ha brillado una <br />
    gran luz.</strong></p>
  <p>Luz de un ni&ntilde;o fr&aacute;gil, que nos <br />
    hace fuertes, luz de un ni&ntilde;o pobre <br />
    que nos hace ricos. Luz de un ni&ntilde;o <br />
    esclavo que nos hace libres, esa <br />
    luz un d&iacute;a nos diste en Bel&eacute;n.</p>
