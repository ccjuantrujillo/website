<p align="center"><strong>335. SHALOM MORENA </strong></p>
  <p>Ven aqu&iacute; hermano para compartir <br />
    la dicha de cantar juntos, a un <br />
    ser divino, aun ser celestial, &Eacute;l <br />
  est&aacute; esperando para darte amor.</p>
  <p>Canta con el alma, d&eacute;jate llevar, <br />
    olvida cualquier agravio, Dios <br />
    cuida tus pasos, ten confianza en <br />
    &Eacute;l, canta con el coraz&oacute;n.</p>
  <p><strong>Que seas feliz, que seas feliz, <br />
  shalom, shalom, que encuentres <br />
  la paz, que encuentres la paz, <br />
  shalom, shalom. Que llegue la <br />
  paz, a todo tu hogar, shalom, <br />
  shalom. Que Dios viva en ti, que <br />
  Dios viva en ti, shalom, shalom.</strong></p>
  <p>Empecemos juntos para <br />
    transformar, evitando la violencia. <br />
    Este mundo sufre por falta de, <br />
    paz, hagamos esfuerzos para eso <br />
    cambiar.</p>
  <p>Adoremos juntos a nuestro <br />
    Se&ntilde;or con canciones y plegarias, <br />
    Cuando hay m&aacute;s voces, m&aacute;s <br />
    fuerte es el clamor y Dios nos <br />
escuchar&aacute;.
</p>
  