
  <p align="center"><strong>297. BENDIGAMOS AL SE&Ntilde;OR</strong></p>
  <p>Bendigamos al Se&ntilde;or, que nos <br />
    une en caridad y nos nutre con su <br />
    amor, en el pan de la unidad. &iexcl;Oh <br />
  padre Nuestro!</p>
  <p>Conservemos la unidad, que el <br />
    maestro nos mand&oacute;. Donde hay <br />
    guerra que haya paz, donde hay <br />
    odio que haya amor. &iexcl;Oh padre <br />
    Nuestro!</p>
  <p>El Se&ntilde;or nos orden&oacute; devolver el <br />
    bien por mal, ser testigos de su <br />
    amor, perdonando de verdad.&iexcl;Oh <br />
    padre Nuestro!</p>
  <p>Al que vive en el dolor y al <br />
    que sufre en soledad, entreguemos <br />
    nuestro amor y consuelo fraternal. <br />
    &iexcl;Oh padre Nuestro!</p>
  <p>El Se&ntilde;or que nos llam&oacute; a vivir en <br />
    unidad, nos congregue con su <br />
    amor en feliz eternidad. &iexcl;Oh padre <br />
    Nuestro!</p>
