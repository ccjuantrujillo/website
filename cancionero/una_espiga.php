
  <p align="center"><strong>100. UNA ESPIGA</strong></p>
  <p>Una espiga dorada por el sol, el <br />
    racimo que corta el vi&ntilde;ador, se <br />
    convierten ahora en pan y vino <br />
    de amor, en el Cuerpo y la Sangre <br />
  del Se&ntilde;or.</p>
  <p>Compartimos la misma comuni&oacute;n, <br />
    somos trigo del mismo sembrador, <br />
    un molino, la vida, nos tritura con <br />
    dolor, Dios nos hace Eucarist&iacute;a en <br />
    el amor.</p>
  <p>Como granos que han hecho el <br />
    mismo pan, como notas que tejen<br />
    un cantar, como gotas de agua <br />
    que se funden en el mar, los <br />
    cristianos un cuerpo formar&aacute;n.</p>
  <p>En la mesa de Dios se sentar&aacute;n, <br />
    como hijos su pan comulgar&aacute;n, <br />
    una misma esperanza caminando <br />
    cantar&aacute;n. En la vida, como <br />
  hermanos se amar&aacute;n.</p>
