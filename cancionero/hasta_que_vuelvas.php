
  <p align="center"><strong>325. HASTA QUE VUELVAS</strong><br />
      <em>(Padre Gabriel Rojas)</em></p>
  <p>Te esperare, hasta que vuelvas, <br />
    te esperare, al llegar de nuevo el <br />
    sol, que alumbres todos mis d&iacute;as, <br />
  con tu luz llena de vida, de vida.</p>
  <p>Te esperare, hasta que vuelvas,<br />
    te esperar&eacute;, en   cada llamada<br />
    del viento, aunque que sea un <br />
    remolino, con todo yo har&eacute; un <br />
    aliento, de vida</p>
  <p>Te	esperare,	hasta	que	vuelvas,<br />
    lo	har&eacute;,	en	cada	grito	de<br />
    esperanza,	en	medio	de	a&ntilde;os<br />
    y siglos mi vida es toda una <br />
    espera, de vida.</p>
  <p>Te	esperare,	hasta	que	vuelvas,<br />
    te esperar&eacute;, en las alas del tiempo <br />
    y al evocar tus memorias, tu <br />
    recuerdo sera mi canto, de vida.<br />
  </p>

