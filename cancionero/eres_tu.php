
  <p align="center"><strong>289. ERES T&Uacute;</strong></p>
  <p>Como una promesa eres T&uacute;, eres <br />
    T&uacute;. Como una ma&ntilde;ana de verano, <br />
    como una sonrisa eres T&uacute;, eres T&uacute;, <br />
  as&iacute;, as&iacute;, eres T&uacute;.</p>
  <p>Toda mi esperanza eres T&uacute;, eres <br />
    T&uacute;. Como lluvia fresca en mis <br />
    manos, como fuerte brisa eres T&uacute;,<br />
    eres T&uacute;, as&iacute;, as&iacute;, eres T&uacute;.</p>
  <p><strong>Eres T&uacute;, como el agua de mi <br />
  fuente, eres T&uacute;, el fuego de mi <br />
  hogar. Eres T&uacute;, como el fuego <br />
  de mi hoguera, eres T&uacute;, el trigo <br />
  de, mi pan</strong>.</p>
  <p>Todo mi poema eres T&uacute;, eres T&uacute;. <br />
    Como una guitarra en la noche, <br />
    todo mi horizonte eres T&uacute;, eres <br />
    T&uacute;, as&iacute;, as&iacute;, eres T&uacute;.<br />
  </p>

