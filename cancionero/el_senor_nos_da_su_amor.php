
  <p align="center"><strong>166. EL SENOR NOS DA SU AMOR </strong></p>
  <p>El se&ntilde;or nos da su amor, como <br />
    nadie nos lo dio, &Eacute;l nos gu&iacute;a como <br />
    estrella en la inmensa oscuridad, <br />
    al partir juntos el pan, &Eacute;l nos llena <br />
    de su amor, pan de Dios, el pan <br />
  comemos de amistad.</p>
  <p><strong>Es mi cuerpo, tomad y comed, <br />
    Es mi sangre, tomad y bebed, <br />
    Porque Yo, soy vida, Yo soy <br />
    amor, &iexcl;Oh! Se&ntilde;or!, nos <br />
    reuniremos en tu amor.</strong></p>
  <p>El Se&ntilde;or nos da su amor, como <br />
    nadie nos lo dio, era tan grande y <br />
    tan hondo, que muri&oacute; sobre una <br />
    cruz. Era tan fuerte su amor, que <br />
    de la muerte triunf&oacute;, de la tumba <br />
    sale libre y vencedor.</p>
  <p>El Se&ntilde;or nos da su amor, como <br />
    nadie nos lo dio, como todos <br />
    sus amigos trabajaba en Nazaret. <br />
    Carpintero se alegr&oacute;, trabajando <br />
    en su taller, con sus manos, Cristo <br />
    obrero trabaj&oacute;</p>
  <p>El Se&ntilde;or nos da su amor, como <br />
    nadie nos lo dio, &Eacute;l re&uacute;ne a los<br />
    amigos en la mesa del amor.<br />
    En el mundo todos son, carne<br />
    y sangre del Se&ntilde;or, nada puede <br />
    separarnos de su amor.</p>
