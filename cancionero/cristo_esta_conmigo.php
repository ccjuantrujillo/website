
  <p align="center"><strong>223. CRISTO EST&Aacute; CONMIGO</strong></p>
  <p><strong>Cristo    est&aacute;    conmigo,    junto    a<br />
  m&iacute;, va el Se&ntilde;or, me acompa&ntilde;a <br />
  siempre, en mi vida hasta el fin.</strong></p>
  <p>Ya no temo, Se&ntilde;or, la tristeza, <br />
    ya no temo, Se&ntilde;or, la soledad. <br />
    Porque eres, Se&ntilde;or, mi alegr&iacute;a, <br />
    tengo siempre tu amistad.</p>
  <p>Ya no temo, Se&ntilde;or a la noche; <br />
    ya no temo, Se&ntilde;or, la oscuridad; <br />
    porque brilla tu luz en las sombras <br />
    ya no hay noche T&uacute; eres luz.</p>
  <p>Ya no temo, Se&ntilde;or, los fracasos; <br />
    ya no temo, Se&ntilde;or, la ingratitud; <br />
    Porque el triunfo, Se&ntilde;or, en la <br />
    vida, T&uacute; lo tienes, T&uacute; lo das.</p>
