
  <p align="center"><strong>262. SOMO LOS NI&Ntilde;OS CANTORES  </strong></p>
  <p>Somos	los	ni&ntilde;os	cantores,<br />
    que vamos a pregonar, la <br />
    natividad se&ntilde;ores, del rey <br />
  de la humanidad.</p>
  <p>Cantar cantores, que esta noche <br />
    es noche buena, vamos a ver el <br />
    nacimiento de una estrella. Cantar <br />
    cantores que ha nacido un <br />
    chiquit&iacute;n, el m&aacute;s hermoso que <br />
    se ha visto por aqu&iacute;.</p>
  <p>Danzad  pastores  que  esta  noche<br />
    es noche   buena, hacer que<br />
    suenen platillos y panderetas.<br />
    Danzad pastores que ha nacido<br />
    un chiquit&iacute;n, el m&aacute;s   hermoso<br />
    que se ha visto por aqu&iacute;</p>
