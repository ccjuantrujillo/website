
  <p align="center"><strong>32. TU QUE SIEMPRE NOS PERDONAS</strong></p>
  <p>T&uacute;, que siempre nos perdonas, <br />
    porque nos quieres mucho. T&uacute; <br />
    que siempre nos perdonas, <br />
  Se&ntilde;or ten piedad.</p>
  <p>T&uacute;, que siempre nos escuchas, <br />
    porque nos quieres mucho. T&uacute; <br />
    que siempre nos escuchas, <br />
    risto ten piedad.</p>
  <p>T&uacute;, que siempre nos ayudas <br />
    porque nos quieres mucho. T&uacute; <br />
    que siempre nos ayudas, Se&ntilde;or <br />
    ten piedad.</p>
