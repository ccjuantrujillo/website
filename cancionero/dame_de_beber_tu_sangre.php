
  <p align="center"><strong>363. DAME DE BEBER TU SANGRE </strong></p>
  <p>Se&ntilde;or, T&uacute; eres mi Dios a Ti yo te busco<br />
  /mi alma tiene sed y hambre de Ti. (2)</p>
  <p>DAME DE BEBER TU SANGRE<br />
    DAME DE COMER TU CUERPO, SE&Ntilde;OR.<br />
    ALIMENTA MI ALMA PARA ALABARTE<br />
    POR SIEMPRE MI BUEN DIOS. (2)</p>
  <p>Pues, es mejor tu amor que la existencia,<br />
    /tu alabanza cantar&eacute; para bendecirte. (2)</p>
  <p>Loas, mis labios cantar&aacute;n para bendecirte,<br />
    /y levantar mis manos en tu nombre, Se&ntilde;or. (2)</p>
