<p align="center"><strong>374. TOMAD SE&Ntilde;OR Y RECIBID </strong></p>
  <p><strong>Tomad se&ntilde;or y recibid<br />
  la ofrenda que traigo<br />
  pan y vino al altar<br />
  que pronto se convertira<br />
  en tu cuerpo y tu sangre<br />
  donde tu santidad</strong></p>
  <p>Traigo junto al pan y a este vino<br />
    la alegria que tengo de servirte se&ntilde;or<br />
    te pido que me aceptes con ellos<br />
    gracias por invitarme a esta fiesta de amor.</p>
  <p>Traigo junto al pan y a este vino<br />
    el dolor de las almas que no saben de ti<br />
    tambien las intenciones de tantos<br />
    para que los bendigas y le des de tu paz</p>
