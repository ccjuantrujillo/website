<p align="center"><strong>257. QUE CANTEN LOS NI&Ntilde;OS</strong></p>
  <p><strong>Que canten los ni&ntilde;os, que alcen <br />
  la voz, que hagan al mundo <br />
  escuchar. Que unan sus voces y <br />
  lleguen al sol, en ellos est&aacute; la <br />
  verdad / Que canten los ni&ntilde;os, <br />
  que viven en paz, y aquellos <br />
  que sufren dolor. Que canten <br />
  por ellos, que no cantar&aacute;n, <br />
  porque han apagado su voz.</strong></p>
  <p>Yo canto para me dejen vivir, yo <br />
    canto para que sonr&iacute;a mam&aacute;, yo <br />
    canto porque sea el cielo azul, y yo <br />
    para que no me ensucien el mar.</p>
  <p>Yo canto para los que no tienen pan, <br />
    yo canto para que respeten la flor, <br />
    yo canto pa&rdquo; que el mundo sea feliz, <br />
    yo canto para no escuchar el ca&ntilde;&oacute;n.</p>
  <p><strong>Que canten los ni&ntilde;os...</strong></p>
  <p>Yo canto porque sea verde el <br />
    jard&iacute;n, y yo para que no me <br />
    apaguen el sol, yo canto por el <br />
    que no sabe escribir, y yo para <br />
    el que escribe versos de amor.</p>
  <p>Yo canto para que se escuche <br />
    mi voz, y yo para ver si les hago <br />
    pensar, yo canto porque quiero un <br />
    mundo feliz, y yo por si alguien <br />
    me quiere escuchar.</p>
 