
  <p align="center"><strong>17. AL PASO DE LOS POBRES </strong></p>
  <p><strong>Caminando va, lentamente el <br />
    pueblo, caminando va, y reza <br />
  una oraci&oacute;n.</strong></p>
  <p>Canci&oacute;n de libertad, canci&oacute;n que <br />
    hable de amor, canci&oacute;n que no le <br />
    mienta, y diga lo que sienta, <br />
    en el coraz&oacute;n.</p>
  <p>La vida ya es muy dura, para <br />
    cantar dolor, cantemos la esperanza <br />
    que nos da alegr&iacute;a, y fecunda el <br />
    amor.</p>
  <p>Al paso de los ni&ntilde;os, tambi&eacute;n sus <br />
    madres van, al paso de los pobres <br />
    el amor invita, hoy a caminar.</p>
