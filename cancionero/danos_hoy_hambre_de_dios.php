
  <p align="center"><strong>193. DANOS HOY HAMBRE DE DIOS </strong></p>
  <p><strong>Danos hoy, hambre de Dios, <br />
  alim&eacute;ntanos Se&ntilde;or, y que el <br />
    fruto de tu amor, limpie el <br />
    rencor, nos de la paz, traiga <br />
  el perd&oacute;n.</strong></p>
  <p>Hacia Ema&uacute;s iban dos amigos, <br />
    sintiendo gran tristeza por Jes&uacute;s, <br />
    y no supieron, que el mismo <br />
    Cristo, era quien iba, en su <br />
    camino.</p>
  <p>La noche est&aacute; muy avanzada, <br />
    dijeron los amigos de Ema&uacute;s, y <br />
    cuando vieron, el pan partido, <br />
    reconocieron, a Cristo vivo.</p>
  <p>El pan que  todos  compartimos<br />
    en una misma comuni&oacute;n,  es  el<br />
    encuentro, con Cristo hermano,<br />
    que dio su vida,  para salvarnos.</p>
  <p>El sembrador sali&oacute; a su campo, <br />
    buscando el fruto del trabajo, <br />
    vio las espigas, que florec&iacute;an, <br />
    pens&oacute; en sus hijos, con alegr&iacute;a.</p>
  <p>Si el don de Dios t&uacute; conocieras, <br />
    le dijo Cristo a la mujer: &ldquo;Le <br />
    pedir&iacute;as, el agua viva, que <br />
    siempre sacia la sed de vida&rdquo;.</p>
  <p>&ldquo;Quien come el pan que yo le <br />
    diera, eternamente vivir&aacute;&rdquo;; unos <br />
    se fueron, desalentados, los que <br />
    quedaron, la fe encontraron.</p>
  <p>&ldquo;Yo soy el pan que da la vida&rdquo;, <br />
    nos dijo a todos el Se&ntilde;or; y <br />
    aquella noche, mientras com&iacute;an, <br />
    el pan de vida, se dio en comida.</p>
