
  <p align="center"><strong>290. SALMO DE LA CREACI&Oacute;N</strong></p>
  <p>Por tu oc&eacute;ano azul, y las aguas <br />
    del mar, por todo continente y los <br />
    r&iacute;os que van. Por el fuego que <br />
    viste, como arbusto ardiente, por <br />
  el ala del viento</p>
  <p><strong>Quiero gritar mi Dios, T&uacute; eres <br />
    grande y hermoso, Dios viviente <br />
    e inmenso, T&uacute; eres el Dios de <br />
    amor. Mi Dios, T&uacute; eres grande y <br />
    hermoso Dios viviente e inmenso <br />
    Dios presente, en toda creaci&oacute;n.</strong></p>
  <p>Y por los animales, de la tierra y <br />
    el agua, por el canto del ave y el <br />
    cantar de la vida / Por el hombre <br />
    que hiciste semejante a Ti, y por <br />
    todos tus hijos.</p>
  <p>Por la mano tendida, que te invita<br />
    a la danza, por el beso que brota <br />
    al surgir la esperanza / La mirada <br />
    de amor, que levanta y reanima, <br />
    por el viento y el pan.<br />
  </p>

