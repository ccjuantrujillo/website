
  <p align="center"><strong>266. NOCHE DE PAZ</strong></p>
  <p>La navidad vuelca su amor, y en <br />
    Bel&eacute;n nace el sol. Canta el cielo, <br />
    la tierra y el mar, y los &aacute;ngeles <br />
    quieren so&ntilde;ar, con un mundo <br />
  mejor, lleno de luz y de paz.</p>
  <p>Lleva	al	cantar,	una	oraci&oacute;n,<br />
    ya naci&oacute; nuestro bien. En la nieve <br />
    las rosas de abril, floreciendo <br />
    con brotes de fe, y un milagro de <br />
    amor, llega al portal de Bel&eacute;n.</p>
  <p>Noche de paz, gloria al Se&ntilde;or, <br />
    una cruz cubre el mar. Golondrinas <br />
    volar junto a &Eacute;l, que no cubra <br />
    de espinas su sien, que Jes&uacute;s nos <br />
    traer&aacute; con su perd&oacute;n la verdad, <br />
    con su perd&oacute;n la verdad.</p>
