<p align="center"><strong>338. UNA COPLA DE PERD&Oacute;N </strong></p>
  <p>Se&ntilde;or tu piedad yo pido, y hoy te <br />
    vengo a implorar, Siento tu amor <br />
    infinito, expresado en el altar. <br />
    Tu clemencia por los siglos, T&uacute; <br />
    me puedas ya brindar. Y es que <br />
    estoy arrepentido, T&uacute; sabes que <br />
    es la verdad. De rodillas, yo te <br />
    pido, que me puedas ayudar, a <br />
    quedarme, en buen camino, lejos <br />
  de toda maldad.</p>
  <p>Cristo amigo T&uacute; que eres, nuestro <br />
    gu&iacute;a al andar, confeso y reconocido <br />
    te digo mi mal obrar. Porque T&uacute; <br />
    eres el Hijo, que al Padre has de <br />
    hablar, para qu&eacute; a todos, derrame, <br />
    su infinita piedad. T&uacute; que eres, el <br />
    camino, fuente de toda verdad, <br />
    Danos Hijo, esperanza, y as&iacute; <br />
    podamos cambiar.</p>
  <p>Hoy	Padre	ya	tus	hijos,	todos<br />
    te van a cantar, en muestra de <br />
    compromiso, el mal, van a evitar. <br />
    Esp&iacute;ritu yo te pido, que me ayudes <br />
    al obrar y que nuestros corazones,<br />
    de	verdad	puedan	cambiar.<br />
    Te pedimos, que nos mandes, <br />
    muchas ganas de cambiar, <br />
    fortaleza, esperanza, para amarnos <br />
    de verdad.</p>
  <p>Te pedimos, por Misiones, por <br />
    todos los curitas, que bendigas, <br />
    las familias y &eacute;sta tu comunidad.</p>
