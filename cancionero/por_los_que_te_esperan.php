
  <p align="center"><strong>294. POR LOS QUE TE ESPERAN</strong></p>
  <p>Haz renacer la  esperanza ven,<br />
    ven a   curar las   heridas, ven<br />
    a traer alegr&iacute;a, alim&eacute;ntanos de <br />
    vida. Transforma los corazones, <br />
    cambia el odio por amor, ven a<br />
    llevarte muy lejos, la miseria y el <br />
  terror, ay, ay, ay.</p>
  <p>Te esperan los maltratados, los <br />
    hambrientos y olvidados, te <br />
    esperan los explotados, por <br />
    patrones empachados, te esperan <br />
    los que no tienen, ni prestigio, <br />
    ni respeto, te esperan los <br />
    indefensos, los que mil veces <br />
    han muerto.</p>
