<p align="center"><strong>239. MI ALMA GLORIFICA AL SE&Ntilde;OR</strong></p>
  <p><strong>Mi alma glorifica al Se&ntilde;or mi <br />
  Dios, gozase mi esp&iacute;ritu en mi <br />
  salvador. &Eacute;l es mi alegr&iacute;a, es <br />
  mi plenitud, &Eacute;l es todo para m&iacute;.</strong></p>
  <p>Ha mirado la bajeza de su sierva, <br />
    muy dichosa me dir&aacute;n todos los <br />
    pueblos, porque en m&iacute; ha hecho <br />
    grandes maravillas, el que todo <br />
    lo puede cuyo nombre es Santo.</p>
  <p>Su clemencia se derrama por los <br />
    siglos, sobre aquellos que le temen <br />
    y le aman, despleg&oacute; el gran poder <br />
    de su derecha, dispers&oacute; a los que<br />
    piensan que son algo.</p>
  <p>Derrib&oacute; a los potentados de su <br />
    trono, y ensalz&oacute; a los humildes y <br />
    a los pobres, los hambrientos se <br />
    saciaron de sus bienes y alej&oacute; <br />
    de s&iacute; vac&iacute;os a los ricos.</p>
  