
  <p align="center"><strong>300. UN MILL&Oacute;N DE AMIGOS</strong></p>
  <p>Yo solo quiero mirar los campos, <br />
    Yo solo quiero cantar mi canto, <br />
    pero no quiero cantar solito, yo <br />
  quiero un coro de pajaritos.</p>
  <p><strong>Quiero  llevar este  canto  amigo,<br />
    a  quien  lo pudiera  necesitar.</strong></p>
  <p><strong>Yo quiero tener un mill&oacute;n de <br />
  amigos y as&iacute; m&aacute;s fuerte poder <br />
    cantar (2v).</strong></p>
  <p>Yo solo quiero un viento fuerte, <br />
    llevar mi barco con rumbo norte, <br />
    y en el trayecto voy a pescar, <br />
    para compartir luego arribar.</p>
  <p>Quiero crear la paz del futuro, <br />
    quiero tener un hogar seguro, <br />
    quiero a mi hijo pisando firme, <br />
    cantando alto sonriendo libre.</p>
  <p>Yo quiero amar siempre en esta <br />
    vida, sentir calor de una mano <br />
    amiga. Quiero a mi hermano <br />
    sonrisa al viento, verlo llorar pero <br />
    de contento.</p>
