<p align="center"><strong>385. YO TE AMO SE&Ntilde;OR MI FORTALEZA (SALMO 17)</strong></p>
  <p>Yo te amo, Se&ntilde;or mi fortaleza,<br />
    mi roca, mi baluarte, mi liberador.<br />
    Eres la pe&ntilde;a en que me amparo,<br />
  mi escudo y mi fuerza, mi Salvador.</p>
  <p>En el templo se escuch&oacute; mi voz,<br />
    clam&eacute; por Ti en mi angustia.<br />
    Extendiste tu mano y no ca&iacute;,<br />
    tu poder del enemigo me libr&oacute;.</p>
  <p>Las olas de la muerte me envolv&iacute;an,<br />
    me aguardaba la ruina,<br />
    pero el Se&ntilde;or venci&oacute;.<br />
    Tu eres la luz que me ilumina,<br />
    quien abre mis caminos,<br />
    T&uacute; eres mi Dios.</p>
  <p>Cuando yo invoqu&eacute; tu Nombre,<br />
    con mano poderosa,<br />
    me salv&oacute; tu Amor.<br />
    Son perfectos tus caminos,<br />
    tus manos me sostienen<br />
T&uacute; eres mi Rey.
