<p align="center"><strong>2. LLEGO EL MOMENTO </strong></p>
  <p>Lleg&oacute; el momento de empezar, <br />
    nuevos caminos a buscar, en <br />
    medio de este pueblo nuestro, que <br />
    busca su libertad. Sembraremos <br />
    semillas de igualdad, lucharemos <br />
  por la fraternidad.</p>
  <p><strong>Y en  este pueblo m&iacute;o, tuyo<br />
    y nuestro, nos encontraremos<br />
    buscando la verdad ... Y<br />
    volveremos, como la  ma&ntilde;ana,<br />
    que a la noche aclara, en su <br />
    despertar.</strong></p>
  <p>Con fuego ardiente hay que <br />
    alumbrar, los corazones inflamar <br />
    de amor por esta tierra nuestra <br />
    que tenemos que guardar. De su <br />
    seno mi pueblo nacer&aacute;, nuestros <br />
    brazos con calor lo abrigar&aacute;n.</p>