
<p align="center"><strong>186. NADIE TE AMA COMO YO</strong></p>
<p>Cuanto he esperado este momento, <br />
cuanto he esperado que estuvieras as&iacute;. <br />
Cuanto he esperado que me hablaras, <br />
cuanto he esperado, que vinieras a m&iacute;.</p>
<p>Yo s&eacute; bien lo que has vivido,<br />
  yo s&eacute; bien por qu&eacute; has llorado,<br />
  yo s&eacute; bien lo que has sufrido, <br />
  pues de tu lado no me he ido.</p>
<p>Pues nadie te ama, como yo, <br />
pues nadie te ama, como yo, <br />
mira la cruz, esa es mi m&aacute;s <br />
grande prueba, nadie te ama, como yo.</p>
<p>Pues nadie te ama, como yo, <br />
  pues nadie te ama, como yo, <br />
  mira la cruz, fue por ti fue <br />
  porque te amo, nadie te ama como yo.</p>
<p>Yo s&eacute; bien lo que me dices, <br />
aunque a veces no me hablas, <br />
yo s&eacute; bien lo que en ti sientes, <br />
aunque nunca lo compartas.</p>
<p>Yo  a  tu  lado  he  caminado,  junto<br />
  a	ti	yo	siempre	he	ido,	a&uacute;n	a<br />
  veces	te	he	cargado,	yo	he	sido<br />
tu	mejor	amigo.</p>

