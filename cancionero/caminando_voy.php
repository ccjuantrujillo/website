
  <p align="center"><strong>220. CAMINANDO VOY </strong></p>
  <p>Caminado voy, sin saber muy <br />
    bien, qu&eacute; camino andar, hacia <br />
    d&oacute;nde ir, hoy me pregunt&eacute;, si s&eacute; <br />
    d&oacute;nde estoy, d&oacute;nde comenc&eacute;, <br />
  cu&aacute;l ser&aacute; mi fin, para qu&eacute; vivir.</p>
  <p>Como un r&iacute;o soy, que hacia el <br />
    mar se va, donde va a morir, y <br />
    resucitar, quiero hacer el bien, en <br />
    mi caminar, que a mi paso den <br />
    flores el jard&iacute;n, panes el trigal.</p>
  <p>En mi coraz&oacute;n, luchan sin cesar <br />
    lo que quiero ser y la realidad, en<br />
    mi    coraz&oacute;n,    quiso    Dios    sembrar,<br />
    ansias  de  vivir,  sed  de  plenitud<br />
    y felicidad.  </p>
