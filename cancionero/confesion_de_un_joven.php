<p align="center"><strong>337. CONFESI&Oacute;N DE UN JOVEN </strong></p>
  <p>Soy   joven	sin	experiencia,	no<br />
    s&eacute;	hacer	las	cosas	bien,	pero<br />
    tu	andas	a	mi	lado,	impulsando<br />
  m&iacute;	que  hacer. </p>
  <p><strong>Me	llamas,	que	te	acompa&ntilde;e,<br />
  mas	no   s&eacute;,   qu&eacute;   voy   hacer.<br />
  Mas	espero,  en  tus  palabras,<br />
  que	me	puedan,	dar	poder.</strong></p>
  <p>Varias	veces	me	equivoco,<br />
    con	mi	manera	de	ser,  T&uacute;<br />
    perdonas	mis	locuras,  y   me<br />
    llevas	a	crecer. </p>
  <p><strong>Todo	el	d&iacute;a	me	he quedado,<br />
  en	mil	cosas	por	hacer,<br />
  cuando	llego	a	mi	noche,<br />
  todo  es	desaparecer. </strong><br />
    <br />
    El	mundo	est&aacute;	cambiando,<br />
    ya	no	es   el	mismo	de   ayer,<br />
    pero	tu	voz		no	se	calla,	en<br />
    lo  hondo  de  mi  ser <br />
    <br />
    <strong>Me	llamas	para	seguirte,	no<br />
    para un d&iacute;a o dos, sino para ir<br />
contigo,	anunciando	tu	perd&oacute;n.</strong></p>
