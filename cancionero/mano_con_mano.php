
  <p align="center"><strong>149. MANO CON MANO</strong></p>
  <p><strong>Mano con mano, voces con <br />
    voces, vamos cantando la amistad, <br />
    porque creemos en el hermano, <br />
  vamos cantando la amistad.</strong></p>
  <p>En las ciudades y en las aldeas, <br />
    en las monta&ntilde;as y en las riberas, <br />
    hay gente que sabe amar, de <br />
    verdad, de verdad.</p>
  <p>En el trabajo y en el descanso, <br />
    en la alegr&iacute;a, como en el llanto, <br />
    hay gente que sabe amar, de <br />
    verdad, de verdad.</p>
  <p>En los mayores y en los peque&ntilde;os, <br />
    en los de cerca y en los de lejos, <br />
    hay gente que sabe amar, de <br />
    verdad, de verdad.</p>
