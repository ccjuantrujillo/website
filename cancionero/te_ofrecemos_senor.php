
<p align="center"><strong>112. TE OFRECEMOS SE&Ntilde;OR</strong></p>
<p>Te  ofrecemos  Se&ntilde;or,  este  pan  y<br />
  este  vino,  te  ofrecemos  tambi&eacute;n,<br />
la labor de nuestras vidas.</p>
<p><strong>Te	ofrecemos	Se&ntilde;or  (3v).<br />
