          <p>Es la actitud de reverencia,  dar honor. Es una característica del espíritu humano la capacidad y necesidad  de adorar.<br>
            Juan 4:24 Jesús le dijo a la  samaritana que los verdaderos adoradores adorarían al Padre en………………………….  porque tales adoradores………</p>
          <p>Este es el privilegio precioso  que tiene el creyente que ha sido bautizado en el Espíritu Santo. No debemos  dejar pasar la oportunidad de aclarar el hecho de que Dios no busca adoración.  Él ya la tiene en el Cielo de parte de los ángeles y seres celestiales. Lo que  busca son adoradores. Él busca tener Comunión, amistad y compañerismo con sus  hijos. Para entender mejor leamos los siguientes versículos.</p>
          <p>Éxodo 34:8 Moisés se postro en  el suelo en adoración.<br>
            Salmos 95:6 Venid………………y…………….</p>
          <p>Es la postura con la que más  se identifica la adoración, sin querer decir con ello que sea la única forma de  hacerlo, pues es la actitud de corazón la que determinara si se está adorando o  no. La adoración se la debemos a Dios, Él es digno de recibirla, de que nos  postremos ante El, a veces con lágrimas, como la mujer pecadora en Lucas  7:36-38 dándole a Él, t5al vez lo más valioso que tenía en ese momento, el  frasco de perfume. Otras veces solamente permaneciendo en silencio ante su magnificencia.  Podemos adorarle también cantando con el entendimiento o con Espíritu, cantando  canciones que hemos escuchado antes, o también las que sean producto de nuestro  amor en ese momento.</p>
          <p>Es el momento de intimidad más  profundo con tu Dios, cuando sin limitaciones le expresas todo tu amor y  reverencia, además es el momento en que eres ministrado por su amor en tu  espíritu, emociones y sentimientos suaves o intensos inundan tu ser ante la  presencia del Señor.</p>
          <p>Apocalipsis 4:10-11 Él es  ……………………….</p>
          <p>La alabanza y la adoración se  deben practicar tanto en privado como en la congregación. Esto es agradable al  Señor</p>
