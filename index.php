<p>R. Gloria a ti, Señor.<br>
  En aquel tiempo, mucha gente acompañaba a Jesus; el se volvio y les dijo: Si alguno viene a mi y no me ama mas que a su padre y a su madre, y a su mujer y a sus hijos, y a sus hermanos y a sus hermanas, e incluso a si mismo, no puede ser discipulo mio. Quien no lleve su cruz detras de mi no puede ser discipulo mio. Asi, ¿quien de ustedes, si quiere construir una torre, no se sienta primero a calcular los gastos, a ver si tiene para terminarla?. No sea que, una vez puestos los cimientos, no puede acabarla y se pongan a burlarse de el los que miran, diciendo: &quot;Este hombre empezo a construir y no ha sido capaz de terminar&quot;. ¿O que rey, si va dar batalla a otro rey, no se sienta primero a deliberar si con diez mil hombres podran salir al paso del que le ataca con veinte mil? .<br>
  Y si no puede, cuando el otro esta todavia lejos, envia delegados para pedir condiciones de paz. Lo mismo ustedes: el que no renuncia a todos sus bienes no puede ser discipulo mio.<br>
  Palabra del Señor. R. Gloria a ti, Señor Jesus</p>

