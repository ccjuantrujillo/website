            - Los siembras año por año, como hierba que se renueva: que florece y se renueva por la mañana, y por la tarde se marchita y seca.<br>
            - Enseñanos a calcular nuestros años, para que adquiramos un corazón sensato. Vuévete Señor, ¿hasta cuando?. Ten compasion de tus siervos.<br>
            - Por la mañana sacianos de tu misericordia, y toda nuestra vida sera alegria y jubilo. Baje a nosotros la bondad del Señor y haga prosperas las obras de nuestras manos.</p>

