          <p>R. Gloria a ti, Señor.<br>
            EN aquel tiempo, Jesús fue llevado al desierto por el Espíritu para ser tentado por el diablo. Y después de ayunar cuarenta días con sus cuarenta noches, al fin sintió hambre.<br>
            El tentador se le acercó y le dijo:<br>
            «Si eres Hijo de Dios, di que estas piedras se conviertan en panes».<br>
            Pero él le contestó:<br>
            «Está escrito: “No solo de pan vive el hombre, sino de toda palabra que sale de la boca de Dios”».<br>
            Entonces el diablo lo llevó a la ciudad santa, lo puso en el alero del templo y le dijo:<br>
            «Si eres Hijo de Dios, tírate abajo, porque está escrito: “Ha dado órdenes a sus ángeles acerca de ti y te sostendrán en sus manos, para que tu pie no tropiece con las piedras”».<br>
            Jesús le dijo:<br>
            «También está escrito: “No tentarás al Señor, tu Dios”».<br>
