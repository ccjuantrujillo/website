            reinos del mundo y su gloria, y le dijo:<br>
            «Todo esto te daré, si te postras y me adoras».<br>
            Entonces le dijo Jesús:<br>
            «Vete, Satanás, porque está escrito: “Al Señor, tu Dios, adorarás y a él solo darás culto”».<br>
