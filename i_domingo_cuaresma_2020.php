          <p>Lectura del libro de Génesis 2,7-9; 3,1-7 <br>
            EL Señor Dios modeló al hombre del polvo del suelo e insufló en su nariz aliento de vida; y el hombre se convirtió en ser vivo.<br>
            Luego el Señor Dios plantó un jardín en Edén, hacia oriente, y colocó en él al hombre que había modelado.<br>
            El Señor Dios hizo brotar del suelo toda clase de árboles hermosos para la vista y buenos para comer; además, el árbol de la vida en mitad del jardín, y el árbol del conocimiento del bien y el mal.<br>
            La serpiente era más astuta que las demás bestias del campo que el Señor había hecho. Y dijo a la mujer:<br>
            «¿Conque Dios os ha dicho que no comáis de ningún árbol del jardín?».<br>
            La mujer contestó a la serpiente:<br>
            «Podemos comer los frutos de los árboles del jardín; pero del fruto del árbol que está en mitad del jardín nos ha dicho Dios:<br>
            “No comáis de él ni lo toquéis, de lo contrario moriréis”».<br>
            La serpiente replicó a la mujer:<br>
            «No, no moriréis; es que Dios sabe que el día en que comáis de él, se os abrirán los ojos, y seréis como Dios en el conocimiento del bien y el mal».<br>
            Entonces la mujer se dio cuenta de que el árbol era bueno de comer, atrayente a los ojos y deseable para lograr inteligencia; así que tomó de su fruto y comió. Luego se lo dio a su marido, que también comió.<br>
          Se les abrieron los ojos a los dos y descubrieron que estaban desnudos; y entrelazaron hojas de higuera y se las ciñeron.</p>

