          <p>HERMANOS:<br>
            Lo mismo que por un hombre entró el pecado en el mundo, y por el pecado la muerte, y así la muerte se propagó a todos los hombres, porque todos pecaron...<br>
            Pues, hasta que llegó la ley había pecado en el mundo, pero el pecado no se imputaba porque no había ley. Pese a todo, la muerte reinó desde Adán hasta Moisés, incluso sobre los que no habían pecado con una transgresión como la de Adán, que era figura del que tenía que venir.<br>
            Sin embargo, no hay proporción entre el delito y el don: si por el delito de uno solo murieron todos, con mayor razón la gracia de Dios y el don otorgado en virtud de un hombre, Jesucristo, se han desbordado sobre todos.<br>
            Y tampoco hay proporción entre la gracia y el pecado de uno:<br>
            pues el juicio, a partir de uno, acabó en condena, mientras que la gracia, a partir de muchos pecados, acabó en justicia.<br>
            Si por el delito de uno solo la muerte inauguró su reinado a través de uno solo, con cuánta más razón los que reciben a raudales el don gratuito de la justificación reinarán en la vida gracias a uno solo, Jesucristo.<br>
            En resumen, lo mismo que por un solo delito resultó condena para todos, así también por un acto de justicia resultó justificación y vida para todos.<br>
          Pues, así como por la desobediencia de un solo hombre, todos fueron constituidos pecadores, así también por la obediencia de uno solo, todos serán constituidos justos.</p>
